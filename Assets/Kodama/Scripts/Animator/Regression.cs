public class Regression
{
    public float a_x { get; }
    public float a_y { get; }
    public float a_z { get; }
    public float b { get; }
    public Regression(float a_x,float a_y,float a_z,float b)
    {
        this.a_x = a_x;
        this.a_y = a_y;
        this.a_z = a_z;
        this.b = b;
    }
}
